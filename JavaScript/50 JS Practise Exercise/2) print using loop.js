
// print the numbers from 1 - 5 to console using loop

console.log("Using For Loop: ");
for (let i = 0; i <= 5; i++) {
    console.log(i);
}


console.log("\n\nUsing While Loop:");
let i = 0;
while (i <= 5) {
    console.log(i);
    i++;
}


console.log("\n\nUsing Do While Loop:");

i = 0;
do {
    console.log(i);
    i++;
} while ( i <= 5);