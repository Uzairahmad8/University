// switch statement :
// A switch statement is a control statement that allows you to execute different blocks of code based on the value of an expression

let day = 1;

switch(day){
    case 1:
        console.log("Monday!");
        break;
    case 2:
        console.log("Tuesday!");
        break;
    case 3: 
        console.log("Wednesday!");
        break;
    case 4:
        console.log("Thursday!");
        break;
    case 5: 
        console.log("Friday!");
        break;
    case 6:
        console.log("Saturday!");
        break;
    case 7:
        console.log("Sunday!");
        break;   
    default:
        console.log("Invalid Day!");
        break; 
}

// OutPut : Monday! ;