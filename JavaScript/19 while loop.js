// while loop :
// A while loop is a control statement that repeatedly executes a block of code as long as a certain condition is true.

// find sum of first nth natural numbers 

let num = 10 ;
let sum = 0 ;

let i = 0;
while( i <= num){
    sum+= i ; // sum = sum + i ;
    i++;
}

console.log(sum);
