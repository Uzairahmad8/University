// ternary operator

// In JavaScript, the ternary operator is a shorthand way to write an if-else statement.

// syntax:
// condition ? expressionIfTrue : expressionIfFalse;
// if the condition is true then the expression after ? will be executed;
// if the condition is false then the expression after : will be executed; 

let age = 8;

let drink = age >= 5 ? "coffee" : "milk";

console.log(drink);
